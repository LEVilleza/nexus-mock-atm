    private void forgotaccnum_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forgotaccnum_buttonActionPerformed
        // TODO add your handling code here:
        
        //Declaration
        boolean inputValid=true;//Flag to check whether input is valid
        int num=0,foundindex=-1;
        String input="";//Stores input
        
        do{//Loop to repeatedly ask for user input until valid
            inputValid=true;
            try{
                input=JOptionPane.showInputDialog(this,"Enter your phone number (Ex. 05****: ").replace(" ", "");//Prompt user for input and store in 'input'
            }
            catch(Exception e){
                break;//Break out of loop in case of error
            }
            
            //Input validation
            try{
                num=Integer.parseInt(input);
            }
            catch(NumberFormatException e){
                inputValid=false;
                JOptionPane.showMessageDialog(this,"Must be numeric");
                continue;
            }
            
            if(input.charAt(0)!='0'){
                inputValid=false;
                JOptionPane.showMessageDialog(this,"Must start with 0");
                continue;
            }
            
            if(input.length()!=10){
                inputValid=false;
                JOptionPane.showMessageDialog(this,"Must be 10 digits");
            }
            
        }while(!inputValid);
        
        //Final Check whether input was valid
        if(inputValid){
            
            //Loops through each row of the database to find entered contact number
            for(int j=0;j<dataobj.details_table.getRowCount();j++){
                if(input.equals(dataobj.details_table.getValueAt(j, 3).toString().replace(" ",""))){
                    foundindex=j;//If found, stores row index
                }
            }
            
            //Checks whether contact number was found in database
            if(foundindex!=-1){
                //Shows SMS screen
                new SMS(foundindex).setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Phone number not found");
            }
            
        }
    }//GEN-LAST:event_forgotaccnum_buttonActionPerformed

