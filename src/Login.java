    private void login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_buttonActionPerformed
        // TODO add your handling code here:
        
        //Hide error messages
        accnum_error.setVisible(false);
        pin_error.setVisible(false);
        
        
        //Declaration
        int accnum=0,//Stores account number
            pin=0,//Stores pin number
            foundindex=-1;//Stores row index of entered account number
        
        String strpin=new String(pin_field.getPassword());//Converts char array value from pin input to string
        
        //Flags for validation
        boolean fieldsValid=true,dataValid=true,accnumFound=false,pinFound=false;
        
        
        //Account Number Validation
        if(accnum_field.getText().isBlank()){
            accnum_error.setText("Missing Value");
            accnum_error.setVisible(true);
            fieldsValid=false;
        }
        else if(accnum_field.getText().length()==6){
            try{
                accnum=Integer.parseInt(accnum_field.getText());
            }
            catch(NumberFormatException e){
                accnum_error.setText("Must be numeric");
                accnum_error.setVisible(true);
                fieldsValid=false;
            }
        }
        else{
            accnum_error.setText("Must be 6 digits");
            accnum_error.setVisible(true);
            fieldsValid=false;
        }
        
        //Pin Validation
        if(strpin.isBlank()){
            pin_error.setText("Missing Value");
            pin_error.setVisible(true);
            fieldsValid=false;
        }
        else if(strpin.length()==4){
            try{
                pin=Integer.parseInt(strpin);
            }
            catch(Exception e){
                pin_error.setText("Must be numeric");
                pin_error.setVisible(true);
                fieldsValid=false;
            }
        }
        else{
            pin_error.setText("Must be 4 digits");
            pin_error.setVisible(true);
            fieldsValid=false;
        }
        
        //Checks if all fields are valid using boolean flag
        if(fieldsValid){
            //Matching entered values to database
            
            //Loops through each row of the table
            for(int i=0;i<dataobj.details_table.getRowCount();i++){
                
                //Checks whether accnum is found in the current row
                if(accnum_field.getText().equals(dataobj.details_table.getValueAt(i, 1).toString())){
                    //If found:
                    accnumFound=true;//sets accnumFound flag to true
                    foundindex=i;//stores row containing the matching Account Number
                    
                    //Checks if pin is found in the same row as accnum
                    if(strpin.equals(dataobj.details_table.getValueAt(foundindex, 2).toString())){
                        pinFound=true;
                    }
                    break;
                }
                
            }
            
            
            
            
            
            //Shows error labels for the appropriate errors
            if(accnumFound==false){
                accnum_error.setText("Account Number not found");
                accnum_error.setVisible(true);
                dataValid=false;
            }
            
            if(pinFound==false){
                pin_error.setText("Pin not found");
                pin_error.setVisible(true);
                dataValid=false;
            }
            
            
            //Final check whether both data were found in the table
            if(dataValid){
                accnum_field.setText("");
                pin_field.setText("");
                if(accnum==999999 && pin==1234){//Check if using manager account details
                    new ManagementControl().setVisible(true);//If true, proceeds to management control
                    dispose();
                }
                else{//Else, proceed to customer menu
                    new Home(foundindex).setVisible(true);
                    dispose();
                }
            }
            else{
                
                pin_field.setText("");//Clear pin field
                
                //Display error message and attempts remaining
                JOptionPane.showMessageDialog(this, "Details not found, please enter correct details or register.\nAttempts Remaining: "+(3-attempts), "Remaining attempts",JOptionPane.ERROR_MESSAGE);
                attempts++;//Increments failed attempts by 1
                
            }
            
            
            
            
        }
        else{
            //Clear all fields
            pin_field.setText("");
            
            attempts++;//If not, increments failed attempts by 1
            
            JOptionPane.showMessageDialog(this, "Invalid Login, Attempts Remaining: "+(3-attempts), "Remaining attempts",JOptionPane.ERROR_MESSAGE);
        }
        
        //Check failed attempts
        if(attempts>=3){
            //Simulate card ejection
            JOptionPane.showMessageDialog(this, "Too many failed login attempts, Card has been ejected", "Too many attempts",JOptionPane.ERROR_MESSAGE);
            new Welcome().setVisible(true);
            dispose();
        }
        
        
        
        
        
        
    }//GEN-LAST:event_login_buttonActionPerformed

